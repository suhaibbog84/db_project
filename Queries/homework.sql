FROM DEPARTMENTS
WHERE DEPARTMENT_ID = 10
   OR DEPARTMENT_ID = 20
   OR DEPARTMENT_ID = 130;

-- 1.12 Display DEPARTMENT_NAME of DEPARTMENT_ID NOT BETWEEN 30 - 100
SELECT DEPARTMENT_NAME, DEPARTMENT_ID
FROM DEPARTMENTS
WHERE DEPARTMENT_ID NOT BETWEEN 30 AND 100;

