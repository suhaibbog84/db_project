/*
 1.1 Display Employee_ID , FIRST_NAME ,  LAST_NAME
        for Employee_ID , 100 , 110 , 120
 */
SELECT EMPLOYEE_ID, FIRST_NAME, LAST_NAME
FROM EMPLOYEES
WHERE EMPLOYEE_ID = 100
   OR EMPLOYEE_ID = 110
   OR EMPLOYEE_ID = 120;

/*
 1.1 Display Employee_ID , FIRST_NAME ,  LAST_NAME
        for Employee_ID 100 to 115  excluding 110
 */
SELECT EMPLOYEE_ID, FIRST_NAME, LAST_NAME
FROM EMPLOYEES
WHERE EMPLOYEE_ID BETWEEN 100 AND 115
  AND EMPLOYEE_ID != 110;

-- 1.2 Display Employee with No Manager_ID

SELECT *
FROM EMPLOYEES
WHERE MANAGER_ID IS NULL;

-- 1.3 Display Employee with No DEPARTMENT_ID
SELECT *
FROM EMPLOYEES
WHERE DEPARTMENT_ID IS NULL;

-- 1.4 Display All Employees from Department 40 to 70
SELECT *
FROM EMPLOYEES
WHERE DEPARTMENT_ID BETWEEN 40 AND 70;

/*
 1.5 Display All Employees with JOB_ID ST_CLERK
 AND SALARY more than 3000 ORDER the result by FIRST_NAME
 */
SELECT *
FROM EMPLOYEES
WHERE JOB_ID = 'ST_CLERK'
  AND SALARY > 3000;

/*
 1.6 Display Employee FIRST_NAME , SALARY ,
 COMMISSION_VALUE  (SALARY*COMMISSION_PCT)
    if the Employee has COMMISSION_PCT value
    Order the result in COMMISSION_VALUE high to low
 (can not use alias, you can use index 3)
 */
SELECT FIRST_NAME,
       SALARY,
       (SALARY * COMMISSION_PCT) AS COMMISSION_VALUE
FROM EMPLOYEES
WHERE COMMISSION_PCT != 0
ORDER BY COMMISSION_PCT;

-- 1.7 Display UNIQUE MANAGER_ID
